function dx=assignment_1(t,x)
global c1 c2 c3
dx=zeros(3,1);
dx(1)=x(2)+x(1)^2;
dx(2)=x(3)+exp(x(2))*x(1);
z=zeros(3,1);
a=zeros(2,1);
z(1)=x(1);
a(1)=-c1*z(1)-x(1)^2;
da1=-c1-2*x(1);
dda1=-2;
z(2)=x(2)-a(1);
a(2)=-z(1)-c2*z(2)-exp(x(2))*x(1)+da1*(x(2)+x(1)^2);
% a(2)=-z(1)-c2*z(2)-exp(x(2))*x(1)-c1*(-c1*z(1)+z(2))-2*x(1)*dx(1);
da2(1,1)=-1+c2*da1-exp(x(2))+da1*2*x(1)+dda1*(x(2)+x(1)^2);
da2(2,1)=-c2-exp(x(2))*x(1)+da1;
z(3)=x(3)-a(2);
u=-z(2)-c3*z(3)-x(1)^2*x(3)^3+...
   da2(1,1)*(x(2)+x(1)^2)+...
   da2(2,1)*(x(3)+exp(x(2))*x(1));
% u=-z(2)-c3*z(3)-x(1)^2*x(3)^3+(1-c1^2)*(-c1*z(1)+z(2))-(c1+c2)*(-z(1)-c2*z(2)+z(3))-...
%     exp(x(2))*x(1)*dx(2)-exp(x(2))*dx(1)-2*dx(1)^2-2*x(1)*(dx(2)+2*x(1)*dx(1));
dx(3)=u+x(1)^2*x(3)^3;
V=0.5*z'*z;
dV=-c1*z(1)^2-c2*z(2)^2-c3*z(3)^2;
end




















































































































